static void gen_char_aa(uint32_t *cpix, int dy)
{
  int y;
  uint32_t *cp;
  char *n0;
  char n_ctr[16*20] = { 0 };

  // count neighbour
  y = dy;
  cp = cpix;
  n0 = &n_ctr[16+1];
  while (y--)
  {
    uint32_t s = *(cp++);
    char *n = n0;
    while (s)
    {
      if (s & 1)
      {
        n[-1]++;
        n[1]++;
        n[-16]++;
        n[16]++;
      }
      s >>= 1;
      n++;
    }
    n0 += 16;
  }

  // clear char pixels, define mask
  y = dy;
  cp = cpix;
  n0 = &n_ctr[16+1];
  while (y--)
  {
    uint32_t s = *cp;
    uint32_t aa = 0;
    uint32_t m = 1;
    char *n = n0;
    while (s)
    {
      if (!(s & 1) && (*n > 1))
        aa |= m;
      n++;
      s >>= 1;
      m <<= 1;
    }
    if (*n > 1)
      aa |= m;
    *cp |= (aa << 16);
    n0 += 16;
    cp++;
  }
}

static void font_gen_aa(font_t *fnt)
{
  int n_char = (128-32) + (fnt->extend ? strlen(fnt->extend) : 0);
  int sz_raw = 1 + fnt->dy;
  uint32_t *cpix = fnt->bit_mask + sz_raw;
  int i;
  for (i=1; i<n_char; i++)                       // 0: space
  {
    gen_char_aa(cpix+1, fnt->dy);                // skip char width
    cpix += sz_raw;
  }
  W_ASSERT(*cpix == 0);
}

// define aa color
// intensity range 0:(no effect) 128:(copy)
pix_t font_get_aa_color(pix_t color, pix_t bk_color, int intensity)
{
  int ia = intensity;
  int ib = 128 - intensity;
  int r = ((color &     0xff)*ia + (bk_color &     0xff)*ib) >> 7;
  int g = ((color &   0xff00)*ia + (bk_color &   0xff00)*ib) >> 7;
  int b = ((color & 0xff0000)*ia + (bk_color & 0xff0000)*ib) >> 7;
  return (r & 0xff) | (g & 0xff00) | (b & 0xff0000);
}

void font_init_aa(void)
{
  font_gen_aa(&f_BOLD7);
  font_gen_aa(&f_BOLD8);
  font_gen_aa(&f_NORM7);
  font_gen_aa(&f_THM8);
  font_gen_aa(&f_THM9);
  font_gen_aa(&f_NORM);
}


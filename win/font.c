// 2 pixels space char '\2', used to display spaces in big numbers
static const uint32_t spc2[17] = { 0x2, 0x000,0x000,0x000,0x000,0x000,0x000,0x000,0x000,
                                        0x000,0x000,0x000,0x000,0x000,0x000,0x000,0x000 };

// find char data bits. if not found return space.
static const uint32_t *get_char_bitmask(const font_t *fnt, char c)
{
  int dy = fnt->dy + 1;
  if ((uint8_t)c < 32)
  {
    if (c == '\2')
      return spc2;
    c = ' ';
  }
  else
  if ((uint8_t)c > 127)                          // check extended chars list
  {
    char ec = c;                                 // save extended code
    c = '-';                                     // define safe one
    if (fnt->extend)
    {
      const uint32_t *bitmask = fnt->bit_mask + (128-32)*dy;
      const char *e = fnt->extend;
      while (*e)
      {
        if (*e == ec)
          return bitmask;
        e++;
        bitmask += dy;
      }
    }
  }
  return fnt->bit_mask + (c - 32)*dy;
}

#define CHAR_WIDTH(fnt, c) (fnt->dx ? ((c == '\2') ? 2 : fnt->dx) : *get_char_bitmask(fnt, c))

// return width of char in pixels
int font_get_char_width(char c, const font_t *fnt)
{
  return CHAR_WIDTH(fnt, c);
}

// truncate to max pixel width, return new len in char unit
int font_string_truncate(char *str, const font_t *fnt, int dx_max)
{
  int l = 0;
  char *s = str;
  for (; *s; s++)
  {
    int w = CHAR_WIDTH(fnt, *s);
    int l1 = l + w;
    if (l1 > dx_max)
    {
      *s = 0;                                    // truncate
      break;
    }
    l = l1;
  }
  return s - str;
}

// return width of string in pixels
int font_get_string_width(const char *str, const font_t *fnt)
{
  int l;
  for (l=0; *str; str++)
    l += CHAR_WIDTH(fnt, *str);
  return l;
}

// use max_chars len, return width of string in pixels
int font_get_string_width_mc(const char *str, const font_t *fnt, int max_chars)
{
  int l;
  for (l=0; *str && (max_chars > 0); str++, max_chars--)
    l += CHAR_WIDTH(fnt, *str);
  return l;
}

#define PIX_ADDR(x, y) (bm->pix_ptr + y*bm->l_size + x)

// display a char with anti aliasing, return char width, 0 if clipped
int bm_draw_raw_char(bitmap_t *bm, int x, int y, pix_t color, pix_t aa_color, const uint32_t *cpix, int dy)
{
  int cw = *cpix++;                              // char width is 1st data
  int xm = x + cw;
  int ym = y + dy;

  // frequent case no clipping required
  if ((x >= 0) && (xm <= bm->size.x) && (y >= 0) && (ym <= bm->size.y))
  {
    pix_t *d0 = PIX_ADDR(x, y);

    if (!aa_color)                               // no aa
    {
      while (dy--)
      {
        uint32_t s = *(cpix++) & 0xFFFF;
        pix_t *d = d0;
        while (s)
        {
          if (s & 1)
            *d = color;
          d++;
          s >>= 1;
        }
        d0 += bm->l_size;
      }
    }
    else
    {
      while (dy--)
      {
        uint32_t s = *(cpix++);
        pix_t *d = d0;
        while (s)
        {
          if (s & 0x10000)
          {
            *d = aa_color;
            s = (s >> 1) & ~0x8000;
          }
          else
          {
            if (s & 0x1)
              *d = color;
            s >>= 1;
          }
          d++;
        }
        d0 += bm->l_size;
      }
    }
    return cw;
  }
  return 0;
}

// display a char, return char with, 0 if clipped
int bm_draw_char(bitmap_t *bm, int x, int y, char c, pix_t color, pix_t aa_color, const font_t *fnt)
{
  const uint32_t *cpix = get_char_bitmask(fnt, c);
  return bm_draw_raw_char(bm, x, y, color, aa_color, cpix, fnt->dy);
}

// draw string, returns the drawn length in pixels
int bm_draw_string(bitmap_t *bm, int x, int y, const char *str, pix_t color, pix_t aa_color, const font_t *fnt, int dx_max)
{
  int x0 = x;
  int xmax = bm->size.x;
  if (dx_max >= 0)                               // < 0 => no limit defined
  {
    int xmax_u = x + dx_max;
    if (xmax_u < xmax)
      xmax = xmax_u;
  }

  while (*str)
  {
    const uint32_t *cpix = get_char_bitmask(fnt, *str++);
    int x1 = x + *cpix;
    if (x1 > xmax)
      break;
    if (!bm_draw_raw_char(bm, x, y, color, aa_color, cpix, fnt->dy))
      break;                                     // clipped
    x = x1;
  }
  return x - x0;
}

// draw and truncate if not enough size to display, then add '...' at end
int bm_draw_string_truncate(bitmap_t *bm, int x, int y, const char *str, pix_t color, pix_t aa_color, const font_t *fnt, int dx_max)
{
  int x0 = x;
  int dot3_w = CHAR_WIDTH(fnt, '.') * 3;
  while (*str)
  {
    int w = CHAR_WIDTH(fnt, *str);
    if (dx_max >= (w + dot3_w))
    {
      if (!bm_draw_char(bm, x, y, *str++, color, aa_color, fnt))
        break;
      x += w;
      dx_max -= w;
    }
    else
    {
      int l = font_get_string_width(str, fnt);
      if (l > dx_max)
        str = "...";
      x += bm_draw_string(bm, x, y, str, color, aa_color, fnt, dx_max);
      break;
    }
  }
  return x - x0;
}

// get mouse click cursor position in string
int font_get_str_click_ofs(const char *str, const font_t *fnt, int dx, int *str_ofs)
{
  const char *s = str;
  int x0 = 0;
  while (*s)
  {
    int w = CHAR_WIDTH(fnt, *s);
    int x1 = x0 + w;
    if (x1 >= dx)
    {
      if (dx >= (x0 + w/2))       // snap to next char
      {
        s++;
        x0 = x1;
      }
      break;
    }
    x0 = x1;
    s++;
  }
  *str_ofs = s - str;
  return x0;
}

// evaluate size of message in pixels
void font_eval_text_rect(const char *text, const font_t *f, int line_dy, vec2i *size)
{
  const char *s;
  int dx = 0;
  size->x = 0;
  size->y = f->dy;
  for (s=text; *s; s++)
  {
    if (*s == '\n')
    {
      size->y += line_dy + f->dy;
      if (dx > size->x)
        size->x = dx;
      dx = 0;
    }
    else
      dx += font_get_char_width(*s, f);
  }
  if (dx > size->x)
    size->x = dx;
}


